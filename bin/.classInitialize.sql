SELECT LOWER(idx.RDB$FIELD_NAME) AS PK
FROM RDB$RELATION_CONSTRAINTS tc
	JOIN RDB$INDEX_SEGMENTS idx ON (idx.RDB$INDEX_NAME = tc.RDB$INDEX_NAME)
WHERE 
	tc.RDB$CONSTRAINT_TYPE = 'PRIMARY KEY' AND 
	idx.RDB$FIELD_NAME != 'EMPRESA' AND
	tc.RDB$RELATION_NAME = 'CADASTRO' ORDER BY idx.RDB$FIELD_POSITION;